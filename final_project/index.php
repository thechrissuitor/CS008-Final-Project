<h2>Why You Should Care</h2>
    <p>
        Dogs today are beaten, chained, and starved. The amount of abuse, between 
        neglectful and abusive owners all the way up through dog fighting rings, 
        is unimaginably immense and wrong. They go through horrendous amounts of 
        trauma and cruelty, and families refuse to take them because they are 
        left physically and mentally scarred, even though a family is the key 
        to getting them better. The purpose of Save Our Sweeties is the inspire
        families looking for dogs to adopt to rescue them. If you’re already
        looking for a dog, then all you need to do is take in a single dog to
        change the course of its life. As a result, if you take in a sad, broken
        dog, then your reward will be much greater than simply adopting a clean
        puppy from the nearest pet store. These dogs have had everything taken 
        from them, and your love, care, and affection can save their lives. 
        To a recovering rescued dog, you love will be the most important thing 
        in their life, and, in return, the dog shall love you unconditionally. 
        Please browse our website to get an idea of what these dogs go through.<br>
